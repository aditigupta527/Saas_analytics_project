/*8-CAC by Acquisition Channel (Customer Acquisition Cost)
how much marketing money is spent to acquire one customer.
CAC = Total Marketing Spend / Number of Customers Acquired*/

SELECT 
     c.acquisition_channel,
     ROUND(SUM(m.spend_amount) / COUNT(DISTINCT c.customer_id), 2) AS CAC
FROM customers AS c
JOIN marketing_spend AS m
ON c.acquisition_channel = m.channel
GROUP BY c.acquisition_channel
ORDER BY CAC desc;




/*9-LTV Calculation
