/*10-Cohort Analysis
Goal: uper signup cohort.
Customer Retention – The percentage of existing customers who remain active over a certain period
Cohort = a group of users segmented based on similar event
idea:Group customers by the month they first signed up and track their activity over time.*/
WITH cohort_activity AS (
    SELECT customer_id,
           DATE_FORMAT(signup_date,'%y-%m') AS cohort_month
    FROM customers
),
activity_with_cohort AS (
    SELECT m.customer_id,
           m.month,
           m.is_active,
           cc.cohort_month,
           -- Calculate number of months since signup
           TIMESTAMPDIFF(MONTH, c.signup_date, m.month) AS cohort_index,     
           DATE_FORMAT(m.month, '%Y-%b') AS month_name,
           MONTH(m.month) AS month_number
    FROM monthly_activity m
    JOIN customers c
        ON m.customer_id = c.customer_id
    JOIN cohort_activity cc
        ON m.customer_id = cc.customer_id
)
SELECT cohort_month,
       month_name,
       month_number,
       cohort_index,
       COUNT(DISTINCT customer_id) AS active_customers
FROM activity_with_cohort
WHERE is_active = 1
GROUP BY cohort_month, month_name, month_number, cohort_index
ORDER BY cohort_month,month_number,cohort_index;




/*1️1 LTV by Acquisition Channel
Goal: See how much revenue each acquisition channel generates per customer.*/
WITH customer_ltv AS (
    SELECT customer_id,
           SUM(mrr) AS LTV
    FROM subscriptions
    GROUP BY customer_id
)
SELECT c.acquisition_channel,
       round(AVG(cl.LTV),2) AS avg_LTV,
       SUM(cl.LTV) AS total_LTV,
       count(cl.customer_id) as total_customers
FROM customer_ltv cl
JOIN customers c
    ON cl.customer_id = c.customer_id
GROUP BY c.acquisition_channel
ORDER BY total_LTV DESC;





/*12-Top plans by total MRR each month*/
select * from(SELECT 
     plan_type,
     DATE_FORMAT(start_date, '%Y-%m') AS month,
	 sum(mrr) as total_mrr,
     dense_rank()over(partition by DATE_FORMAT(start_date, '%Y-%m') order by sum(mrr) desc) as rnk
from subscriptions
group by  plan_type,  DATE_FORMAT(start_date, '%Y-%m')
)ranked
where rnk<=3
ORDER BY month, rnk;





/*13-Marketing Spend vs Revenue*/
Select m.month,
       round(sum(m.spend_amount))as total_marketing_spend,
       round(sum(r.total_mrr))as total_revenue
from marketing_spend as m
join revenue as r
on DATE_FORMAT(m.month, '%Y-%m') = DATE_FORMAT(r.month, '%Y-%m')
group by m.month
ORDER BY month;
       


