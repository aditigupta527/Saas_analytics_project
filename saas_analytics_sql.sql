    COUNT(DISTINCT CASE WHEN is_active = 0 THEN customer_id END) AS inactive_customers
FROM monthly_activity;





/*6-Churned Customers per Month
Percentage of customers who leave the service during a period
A churned customer is someone who stops using your product or service.*/
WITH months AS (
    SELECT DISTINCT DATE_FORMAT(start_date, '%Y-%m') AS month
    FROM subscriptions
),

active_customers AS (
    SELECT 
        m.month,
        COUNT(DISTINCT s.customer_id) AS active_customers
    FROM months m
    JOIN subscriptions s
        ON DATE_FORMAT(s.start_date, '%Y-%m') <= m.month
        AND (
            s.end_date IS NULL 
            OR DATE_FORMAT(s.end_date, '%Y-%m') >= m.month
        )
    GROUP BY m.month
),

churned_customers AS (
    SELECT 
        DATE_FORMAT(end_date, '%Y-%m') AS month,
        COUNT(DISTINCT customer_id) AS churned_customers
    FROM subscriptions
    WHERE end_date IS NOT NULL
    GROUP BY DATE_FORMAT(end_date, '%Y-%m')
)

SELECT 
    a.month,
    a.active_customers,
    COALESCE(c.churned_customers, 0) AS churned_customers,
    
    CONCAT(
        ROUND(
            COALESCE(c.churned_customers, 0) * 100.0 
            / NULLIF(a.active_customers, 0),
            2
        ),
        '%'
    ) AS churn_rate_percentage

FROM active_customers a
LEFT JOIN churned_customers c
    ON a.month = c.month
ORDER BY a.month;




